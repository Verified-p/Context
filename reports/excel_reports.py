from io import BytesIO

from django.http import HttpResponse
from django.utils import timezone

from openpyxl import Workbook
from openpyxl.styles import (
    Font,
    PatternFill,
    Border,
    Side,
    Alignment,
)
from openpyxl.utils import get_column_letter

from accounts.models import User
from attendance.models import (
    AttendanceSession,
    AttendanceRecord,
)
from assignments.models import (
    Assignment,
    AssignmentSubmission,
)


# ==========================================================
# COLORS
# ==========================================================

TITLE_FILL = PatternFill(
    fill_type="solid",
    fgColor="1F4E78"
)

HEADER_FILL = PatternFill(
    fill_type="solid",
    fgColor="D9EAD3"
)

SUMMARY_FILL = PatternFill(
    fill_type="solid",
    fgColor="FFF2CC"
)

WHITE_FONT = Font(
    color="FFFFFF",
    bold=True,
    size=16
)

HEADER_FONT = Font(
    bold=True
)

NORMAL_FONT = Font(
    size=11
)

THIN_BORDER = Border(
    left=Side(style="thin"),
    right=Side(style="thin"),
    top=Side(style="thin"),
    bottom=Side(style="thin"),
)


# ==========================================================
# CREATE WORKBOOK
# ==========================================================

def create_workbook(title):

    workbook = Workbook()

    sheet = workbook.active

    sheet.title = title

    return workbook, sheet


# ==========================================================
# MAIN TITLE
# ==========================================================

def write_title(sheet, title):

    sheet.merge_cells("A1:H1")

    cell = sheet["A1"]

    cell.value = title

    cell.font = WHITE_FONT

    cell.fill = TITLE_FILL

    cell.alignment = Alignment(
        horizontal="center",
        vertical="center"
    )

    sheet.row_dimensions[1].height = 28


# ==========================================================
# REPORT INFORMATION
# ==========================================================

def write_report_information(sheet, generated_by):

    sheet["A3"] = "Generated By"
    sheet["B3"] = generated_by

    sheet["A4"] = "Generated On"
    sheet["B4"] = timezone.localtime().strftime(
        "%d %B %Y %I:%M %p"
    )

    sheet["A3"].font = HEADER_FONT
    sheet["A4"].font = HEADER_FONT


# ==========================================================
# SUMMARY BOX
# ==========================================================

def write_summary(sheet, start_row, summary_data):

    row = start_row

    sheet.cell(
        row=row,
        column=1
    ).value = "Summary"

    sheet.cell(
        row=row,
        column=1
    ).font = HEADER_FONT

    sheet.cell(
        row=row,
        column=1
    ).fill = SUMMARY_FILL

    row += 1

    for label, value in summary_data:

        label_cell = sheet.cell(
            row=row,
            column=1
        )

        value_cell = sheet.cell(
            row=row,
            column=2
        )

        label_cell.value = label
        value_cell.value = value

        label_cell.font = HEADER_FONT

        label_cell.border = THIN_BORDER
        value_cell.border = THIN_BORDER

        row += 1

    return row


# ==========================================================
# TABLE HEADER
# ==========================================================

def write_table_header(
    sheet,
    row,
    headers
):

    for column, header in enumerate(
        headers,
        start=1
    ):

        cell = sheet.cell(
            row=row,
            column=column
        )

        cell.value = header

        cell.font = HEADER_FONT

        cell.fill = HEADER_FILL

        cell.alignment = Alignment(
            horizontal="center",
            vertical="center"
        )

        cell.border = THIN_BORDER


# ==========================================================
# TABLE ROW
# ==========================================================

def write_table_row(
    sheet,
    row,
    values
):

    for column, value in enumerate(
        values,
        start=1
    ):

        cell = sheet.cell(
            row=row,
            column=column
        )

        cell.value = value

        cell.font = NORMAL_FONT

        cell.border = THIN_BORDER


# ==========================================================
# AUTO SIZE COLUMNS
# ==========================================================

def auto_size_columns(sheet):

    for column_cells in sheet.columns:

        length = 0

        letter = get_column_letter(
            column_cells[0].column
        )

        for cell in column_cells:

            try:

                if cell.value:

                    length = max(
                        length,
                        len(str(cell.value))
                    )

            except Exception:

                pass

        sheet.column_dimensions[
            letter
        ].width = min(
            length + 5,
            40
        )


# ==========================================================
# RETURN EXCEL FILE
# ==========================================================

def download_workbook(
    workbook,
    filename
):

    output = BytesIO()

    workbook.save(output)

    output.seek(0)

    response = HttpResponse(
        output.getvalue(),
        content_type=(
            "application/vnd.openxmlformats-"
            "officedocument.spreadsheetml.sheet"
        )
    )

    response[
        "Content-Disposition"
    ] = f'attachment; filename="{filename}"'

    return response


# ==========================================================
# STUDENT REPORT EXCEL
# ==========================================================

def student_report_excel(request):
    """
    Export Student Report to Excel.
    Accessible to Super Admins and Trainers.
    """

    if not (
        request.user.is_staff or
        request.user.role in ["SUPER_ADMIN", "TRAINER"]
    ):
        return HttpResponse(
            "You are not authorized to access this report.",
            status=403
        )

    students = (
        User.objects
        .filter(role="STUDENT")
        .order_by("first_name", "last_name", "username")
    )

    workbook, sheet = create_workbook(
        "Student Report"
    )

    write_title(
        sheet,
        "CETMS STUDENT REPORT"
    )

    write_report_information(
        sheet,
        request.user.get_full_name() or request.user.username
    )

    # ======================================================
    # SUMMARY
    # ======================================================

    total_students = students.count()

    verified_students = students.filter(
        is_verified=True
    ).count()

    unverified_students = (
        total_students - verified_students
    )

    locked_accounts = students.filter(
        is_locked=True
    ).count()

    last_row = write_summary(
        sheet,
        6,
        [

            ("Total Students", total_students),

            ("Verified Students", verified_students),

            ("Unverified Students", unverified_students),

            ("Locked Accounts", locked_accounts),

        ]
    )

    # ======================================================
    # STUDENT TABLE
    # ======================================================

    table_row = last_row + 2

    headers = [

        "No",

        "Username",

        "Full Name",

        "Email",

        "Phone",

        "Verified",

        "Locked",

        "Date Joined",

    ]

    write_table_header(
        sheet,
        table_row,
        headers
    )

    row = table_row + 1

    for number, student in enumerate(
        students,
        start=1
    ):

        full_name = (
            student.get_full_name()
            if student.get_full_name()
            else student.username
        )

        write_table_row(

            sheet,

            row,

            [

                number,

                student.username,

                full_name,

                student.email,

                student.phone_number or "-",

                "Yes" if student.is_verified else "No",

                "Yes" if student.is_locked else "No",

                timezone.localtime(
                    student.date_joined
                ).strftime("%d/%m/%Y"),

            ]

        )

        row += 1

    auto_size_columns(sheet)

    return download_workbook(

        workbook,

        "Student_Report.xlsx"

    )

