# ATTENDANCE REPORT EXCEL
# ==========================================================

def attendance_report_excel(request):
    """
    Export Attendance Report to Excel.
    Accessible to Super Admins and Trainers.
    """

    if not (
        request.user.is_staff or
        request.user.role in ["SUPER_ADMIN", "TRAINER"]
    ):
        return HttpResponse(
            "You are not authorized to access this report.",
            status=403
        )

    sessions = AttendanceSession.objects.count()

    records = (
        AttendanceRecord.objects
        .select_related(
            "student",
            "session"
        )
        .order_by(
            "-timestamp"
        )
    )

    workbook, sheet = create_workbook(
        "Attendance Report"
    )

    write_title(
        sheet,
        "CETMS ATTENDANCE REPORT"
    )

    write_report_information(
        sheet,
        request.user.get_full_name() or request.user.username
    )

    # ======================================================
    # SUMMARY
    # ======================================================

    total_records = records.count()

    present = records.filter(
        status="PRESENT"
    ).count()

    late = records.filter(
        status="LATE"
    ).count()

    absent = records.filter(
        status="ABSENT"
    ).count()

    active_sessions = AttendanceSession.objects.filter(
        is_active=True
    ).count()

    attendance_rate = 0

    if total_records > 0:

        attendance_rate = round(
            (present / total_records) * 100,
            1
        )

    last_row = write_summary(

        sheet,

        6,

        [

            ("Attendance Sessions", sessions),

            ("Active Sessions", active_sessions),

            ("Attendance Records", total_records),

            ("Present", present),

            ("Late", late),

            ("Absent", absent),

            ("Attendance Rate", f"{attendance_rate}%"),

        ]

    )

    # ======================================================
    # TABLE
    # ======================================================

    table_row = last_row + 2

    headers = [

        "No",

        "Student",

        "Course",

        "Session",

        "Status",

        "Recorded At",

        "Trainer",

    ]

    write_table_header(

        sheet,

        table_row,

        headers

    )

    row = table_row + 1

    for number, record in enumerate(
        records,
        start=1
    ):

        trainer = (
            record.session.created_by.get_full_name()
            or record.session.created_by.username
        )

        student = (
            record.student.get_full_name()
            or record.student.username
        )

        write_table_row(

            sheet,

            row,

            [

                number,

                student,

                record.session.course or "-",

                record.session.title,

                record.status,

                timezone.localtime(
                    record.timestamp
                ).strftime(
                    "%d/%m/%Y %H:%M"
                ),

                trainer,

            ]

        )

        row += 1

    auto_size_columns(sheet)

    return download_workbook(

        workbook,

        "Attendance_Report.xlsx"

    )

