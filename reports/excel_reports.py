def assignment_report_excel(request):
    """
    Export Assignment Report to Excel.
    Accessible to Super Admins and Trainers.
    """

    if not (
        request.user.is_staff or
        request.user.role in ["SUPER_ADMIN", "TRAINER"]
    ):
        return HttpResponse(
            "You are not authorized to access this report.",
            status=403
        )

    assignments = Assignment.objects.all()

    submissions = (
        AssignmentSubmission.objects
        .select_related(
            "assignment",
            "student",
            "graded_by"
        )
        .order_by("-submitted_at")
    )

    workbook, sheet = create_workbook(
        "Assignment Report"
    )

    write_title(
        sheet,
        "CETMS ASSIGNMENT REPORT"
    )

    write_report_information(
        sheet,
        request.user.get_full_name() or request.user.username
    )

    # ======================================================
    # SUMMARY
    # ======================================================

    total_assignments = assignments.count()

    total_submissions = submissions.count()

    graded = submissions.filter(
        status="GRADED"
    ).count()

    pending = submissions.filter(
        status="SUBMITTED"
    ).count()

    late = submissions.filter(
        is_late=True
    ).count()

    last_row = write_summary(

        sheet,

        6,

        [

            ("Total Assignments", total_assignments),

            ("Total Submissions", total_submissions),

            ("Graded", graded),

            ("Pending Grading", pending),

            ("Late Submissions", late),

        ]

    )

    # ======================================================
    # TABLE HEADER
    # ======================================================

    table_row = last_row + 2

    headers = [

        "No",

        "Assignment",

        "Student",

        "Marks",

        "Percentage",

        "Status",

        "Late",

        "Submitted At",

        "Graded By",

    ]

    write_table_header(
        sheet,
        table_row,
        headers
    )

    # ======================================================
    # TABLE DATA
    # ======================================================

    row = table_row + 1

    for number, submission in enumerate(
        submissions,
        start=1
    ):

        grader = "-"

        if submission.graded_by:

            grader = (
                submission.graded_by.get_full_name()
                or submission.graded_by.username
            )

        student = (
            submission.student.get_full_name()
            or submission.student.username
        )

        marks = (
            submission.marks_awarded
            if submission.marks_awarded is not None
            else "-"
        )

        percentage = (
            f"{submission.percentage_score}%"
            if submission.marks_awarded is not None
            else "-"
        )

        write_table_row(

            sheet,

            row,

            [

                number,

                submission.assignment.title,

                student,

                marks,

                percentage,

                submission.status,

                "Yes" if submission.is_late else "No",

                timezone.localtime(
                    submission.submitted_at
                ).strftime(
                    "%d/%m/%Y %H:%M"
                ),

                grader,

            ]

        )

        row += 1

    auto_size_columns(sheet)

    return download_workbook(

        workbook,

        "Assignment_Report.xlsx"

    )

